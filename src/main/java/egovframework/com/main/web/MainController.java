@Controller
public class MainController {

	/** MainService */
	@Resource(name = "mainService")
	private MainService mainService;

	/** EgovPropertyService */
	@Resource(name = "propertiesService")
	protected EgovPropertyService propertiesService;

	/** Validator */
	@Resource(name = "beanValidator")
	protected DefaultBeanValidator beanValidator;

	/**
	 * 글 목록을 조회한다. (pageing)
	 * 
	 * @param searchVO - 조회할 정보가 담긴 SearchDefaultVO
	 * @param model
	 * @return "egovMainList"
	 * @exception Exception
	 */
	@RequestMapping(value = "/mainList.do")
	public String selectMainList(@ModelAttribute("searchVO") SearchDefaultVO searchVO, ModelMap model)
			throws Exception {
		System.out.println("1111");
		searchVO.setPageUnit(propertiesService.getInt("pageUnit"));
		searchVO.setPageSize(propertiesService.getInt("pageSize"));

		/** pageing setting */
		PaginationInfo paginationInfo = new PaginationInfo();
		paginationInfo.setCurrentPageNo(searchVO.getPageIndex());
		paginationInfo.setRecordCountPerPage(searchVO.getPageUnit());
		paginationInfo.setPageSize(searchVO.getPageSize());

		searchVO.setFirstIndex(paginationInfo.getFirstRecordIndex());
		searchVO.setLastIndex(paginationInfo.getLastRecordIndex());
		searchVO.setRecordCountPerPage(paginationInfo.getRecordCountPerPage());

		List<?> mainList = mainService.selectMainList(searchVO);
		System.out.println("mainList == " + mainList);
		model.addAttribute("resultList", mainList);

		int totCnt = mainService.selectMainListTotCnt(searchVO);
		paginationInfo.setTotalRecordCount(totCnt);
		model.addAttribute("paginationInfo", paginationInfo);
		System.out.println("222");
		return "main/mainList.tiles";
	}

}
